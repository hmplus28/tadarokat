def _base_payload(purchase: dict) -> dict:
    return {
        "purchase_number": str(purchase.get("شماره خرید") or purchase.get("شماره") or ""),
        "نوع خرید": purchase.get("نوع خرید") or "استعلامی",
        "مهلت استعلام": purchase.get("مهلت استعلام") or "1405/04/15",
        "واحد/رمز تامین": purchase.get("واحد/رمز تامین") or "تدارکات داخلی",
        "رمز فوریت": purchase.get("رمز فوریت") or "عادی",
        "انبار": "انبار مصرفی",
        "درخواست دهنده": purchase.get("درخواست کننده") or "—",
        "شماره درخواست کالا": (
            str(int(purchase["شماره مبنا"]))
            if purchase.get("شماره مبنا") not in (None, "", "nan")
            else str(purchase.get("شماره خرید") or purchase.get("شماره") or "")
        ),
        "تاریخ درخواست کالا": str(purchase.get("تاریخ درخواست کالا") or purchase.get("تاریخ") or "")[:10] or "1405/04/01",
        "تاریخ دریافت": "1405/04/10",
        "علت خرید": "داده تست",
    }


def main() -> None:
    local_storage.ensure_local_workbook()
    created = []
    skipped = []

    for sample in SAMPLES:
        inq_num = sample["inquiry_number"]
        pn = sample["purchase_number"]

        if local_storage.inquiry_exists(inq_num):
            skipped.append(inq_num)
            continue

        purchase = inquiry_service.lookup_purchase(pn)
        if not purchase:
            print(f"SKIP {inq_num}: purchase {pn} not found")
            continue

        if local_storage.purchase_has_local_inquiry(pn, EXPERT):
            existing = local_storage.get_local_inquiry_by_purchase(pn, EXPERT)
            skipped.append(f"{pn}→{existing}")
            continue

        payload = _base_payload(purchase)
        payload["inquiry_number"] = inq_num
        payload["pre_invoices"] = sample["pre_invoices"]

        for pi in payload["pre_invoices"]:
            title = purchase.get("عنوان کالا") or ""
            for line in pi.get("lines") or []:
                if not line.get("عنوان کالا"):
                    line["عنوان کالا"] = title

        user = {"role": "expert", "expert": EXPERT, "username": USERNAME, "name": ISSUER}
        result = inquiry_service.create_inquiry_with_preinvoices(
            payload, ISSUER, USERNAME, user=user
        )
        created.append(inq_num)
        print(f"OK  استعلام {inq_num} — خرید {pn} — {sample['status_note']}")

    invalidate_cache()
    print(f"\nخلاصه: {len(created)} ایجاد شد، {len(skipped)} رد شد")
    if created:
        print("ورود: mostafa / mostafa123")
        print("استعلام‌ها:", ", ".join(created))


if __name__ == "__main__":
    main()